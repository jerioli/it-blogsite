          Protecting yourself online shouldn't have to be so difficult. In fact, small and consistent habits can make a big difference in our life.
        </p>

        <p class="text-sm sm:text-base md:text-lg text-slate-700 leading-relaxed mb-4 sm:mb-6">
          Here are some quick and important cyber safety tips that we can apply in our daily online activities:
        </p>

        <div class="space-y-4 mb-8">
          <div class="bg-gradient-to-r from-blue-50 to-cyan-50 border-l-4 border-blue-600 p-4 sm:p-6 rounded-r-lg">
            <h3 class="text-lg sm:text-xl md:text-2xl lg:text-3xl font-bold text-slate-900 mb-2">Think Before You Click</h3>
            <p class="text-xs sm:text-sm md:text-base text-slate-700">
              Not all links are safe and can be trusted. Take a moment to double check first before clicking, whether the link comes from an email, message, or social media post. If it is something unusual, it's best not to open it.
            </p>
          </div>

          <div class="bg-gradient-to-r from-blue-50 to-cyan-50 border-l-4 border-blue-600 p-4 sm:p-6 rounded-r-lg">
            <h3 class="text-lg sm:text-xl md:text-2xl lg:text-3xl font-bold text-slate-900 mb-2">Create Strong and Unique Passwords</h3>
            <p class="text-xs sm:text-sm md:text-base text-slate-700">
              Make passwords that are difficult to guess by combining letters, numbers, and symbols. It will be better not to use the same password across multiple accounts to prevent hackers from accessing everything at once.
            </p>
          </div>

          <div class="bg-gradient-to-r from-blue-50 to-cyan-50 border-l-4 border-blue-600 p-4 sm:p-6 rounded-r-lg">
            <h3 class="text-lg sm:text-xl md:text-2xl lg:text-3xl font-bold text-slate-900 mb-2">Turn On Extra Security</h3>
            <p class="text-xs sm:text-sm md:text-base text-slate-700">
              Enable features like two-factor authentication, if possible. This can add another layer of security by requiring a second verification step before it allows you to log in.
            </p>
          </div>

          <div class="bg-gradient-to-r from-blue-50 to-cyan-50 border-l-4 border-blue-600 p-4 sm:p-6 rounded-r-lg">
            <h3 class="text-lg sm:text-xl md:text-2xl lg:text-3xl font-bold text-slate-900 mb-2">Keep Your Devices Updated</h3>
            <p class="text-xs sm:text-sm md:text-base text-slate-700">
              Do not ignore notifications wherein it requires you to update your apps, software, and devices because it might be annoying, but it helps in fixing security problems and protects you from possible risks.
            </p>
          </div>

          <div class="bg-gradient-to-r from-blue-50 to-cyan-50 border-l-4 border-blue-600 p-4 sm:p-6 rounded-r-lg">
            <h3 class="text-lg sm:text-xl md:text-2xl lg:text-3xl font-bold text-slate-900 mb-2">Be Careful with What You Share</h3>
            <p class="text-xs sm:text-sm md:text-base text-slate-700">
              Sharing too much personal information on social media can put you at risk. Even simple details about the things you like and what you usually do can be used by wrong people. It is very important to pause and think first before posting anything.
            </p>
          </div>

          <div class="bg-gradient-to-r from-blue-50 to-cyan-50 border-l-4 border-blue-600 p-4 sm:p-6 rounded-r-lg">
            <h3 class="text-lg sm:text-xl md:text-2xl lg:text-3xl font-bold text-slate-900 mb-2">Be Careful with Public Wi-Fi</h3>
            <p class="text-xs sm:text-sm md:text-base text-slate-700">
              It is very convenient if there is free Wi-Fi especially when you are outside, but it is not always safe. Public networks can easily be accessed by others which means that your personal information is not safe. Avoid opening important accounts especially bank apps when you're connected to public networks.
            </p>
          </div>

          <div class="bg-gradient-to-r from-blue-50 to-cyan-50 border-l-4 border-blue-600 p-4 sm:p-6 rounded-r-lg">
            <h3 class="text-lg sm:text-xl md:text-2xl lg:text-3xl font-bold text-slate-900 mb-2">Always Log Out on Shared Devices</h3>
            <p class="text-xs sm:text-sm md:text-base text-slate-700">
              Make it a habit and always check if you have logged out of a device that is not yours. It seems like a small step, but it's to protect you and prevent others from getting access to your information.
            </p>
          </div>

          <div class="bg-gradient-to-r from-blue-50 to-cyan-50 border-l-4 border-blue-600 p-4 sm:p-6 rounded-r-lg">
            <h3 class="text-lg sm:text-xl md:text-2xl lg:text-3xl font-bold text-slate-900 mb-2">Don't Trust Unknown Messages</h3>
            <p class="text-xs sm:text-sm md:text-base text-slate-700">
              Always be cautious of messages you receive from unknown senders especially if it contain links and is routing you to sign in or claim anything. These are fraudulent acts and might further ask for personal information, money, and might also act as someone you knew. If something feels off in the message, just delete and ignore it.
            </p>
          </div>
        </div>

        <div class="bg-slate-900 text-white p-4 sm:p-6 rounded-xl mb-8 border-l-4 border-yellow-400">
          <h3 class="text-lg sm:text-xl md:text-2xl lg:text-3xl font-bold mb-3">Quick Reminder</h3>
          <p class="text-xs sm:text-sm md:text-base text-slate-200">
            It doesn't require you to be an expert or skilled to stay safe online. Most of it just comes down to being careful, alert, and being responsible with whatever activity you do online.
          </p>
        </div>

        <h2 class="text-xl sm:text-2xl md:text-3xl lg:text-4xl font-bold text-slate-900 mb-6 mt-6 sm:mt-8">Real-World Application</h2>

        <p class="text-sm sm:text-base md:text-lg text-slate-700 leading-relaxed mb-4 sm:mb-6">
          These cyber safety tips aren't just rules—they reflect real-life situations we encounter online every day. For instance, students often receive emails claiming to be from their school, which can trick them into sharing login details. Social media users sometimes overshare personal information, making it easier for scammers to guess passwords or impersonate them. Even simple actions like pausing before connecting to public Wi-Fi or logging out of a friend's device can prevent potential problems. Applying these habits helps us navigate the digital world confidently, keeping our personal information and online interactions safe from real threats.
        </p>

