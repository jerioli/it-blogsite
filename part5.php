        <h1 class="text-3xl sm:text-4xl md:text-5xl lg:text-6xl font-bold text-slate-900">
          Part 5: How to Protect Yourself Online
        </h1>
      </div>

      <div class="prose prose-sm sm:prose-base md:prose-lg max-w-none">
        <p class="text-sm sm:text-base md:text-lg text-slate-700 leading-relaxed mb-4 sm:mb-6">
          In today's generation where digital stuff is everywhere, it is necessary to know how we can protect ourselves online. Every time we use technologies and browse in social media, connect to public Wi-Fi, click unfamiliar links etc. we are exposing ourselves to potential cyber threats like phishing, identity theft, malware and online scams.
        </p>

        <p class="text-lg text-slate-700 leading-relaxed mb-6">
          Online security refers to the practices and tools that we can use to protect our personal information, identity and prevent cybercrimes. This means creating strong passwords, enabling two-factor authentication (2FA), and being cautious on suspicious links.
        </p>

        <p class="text-lg text-slate-700 leading-relaxed mb-6">
          Online safety is a concept of being digitally aware. By being mindful in internet such us, clicking, posting, sharing and who we interact with. For the reason that hackers rely on human errors and not on technical issues. Just like phishing—tricking users into giving sensitive or personal information by pretending to be a legitimate person or companies.
        </p>

        <p class="text-lg text-slate-700 leading-relaxed mb-6">
          Additionally, the concept of Data Privacy. Oversharing on social media is harmful and can be an easy target for cyber criminals. That's why private information like name, address, phone number, real-time location and bank details should always be tracked and protected.
        </p>

        <p class="text-lg text-slate-700 leading-relaxed mb-6">
          Therefore, to protect ourselves in the online world, we should know to combine smart behavior with security tools to at least reduce the risk that we can encounter in today's digital world and to be safe while enjoying browsing.
        </p>

        <h2 class="text-xl sm:text-2xl md:text-3xl lg:text-4xl font-bold text-slate-900 mb-6 mt-6 sm:mt-8">Real-World Application</h2>

        <p class="text-lg text-slate-700 leading-relaxed mb-6">
          In our country Philippines, online scams are becoming common, especially on Facebook, messages, and online shopping applications. One of the best examples is "phishing text scams" where users receive messages from someone who's pretending to be from banks or even claiming that you won money from a raffle—asking individuals to click, enter personal information or look for their account online.
        </p>

        <p class="text-lg text-slate-700 leading-relaxed mb-6">
          For instance, way back 2023, the Bangko Sentral ng Pilipinas warned the public about spreading of fake messages pretending to be from BDO or GCash. A lot of Filipinos were tricked by clicking links that led to fake websites, which resulted in stolen bank information and loss of money.
        </p>

        <h3 class="text-lg sm:text-xl md:text-2xl lg:text-3xl font-bold text-slate-900 mb-4 mt-4 sm:mt-6">Here are some useful tips to protect yourself in this kind of situation:</h3>

        <div class="space-y-4 mb-8">
          <div class="bg-green-50 border-l-4 border-green-600 p-4 rounded-r-lg">
            <strong class="text-slate-900">Verify before clicking:</strong>
            <p class="text-slate-700 mt-1">Make it a habit of checking the sender of the messages or emails if it's from a legitimate person or companies.</p>
          </div>

          <div class="bg-green-50 border-l-4 border-green-600 p-4 rounded-r-lg">
            <strong class="text-slate-900">Use strong and unique passwords:</strong>
            <p class="text-slate-700 mt-1">Avoid using predictable passwords like "123456789" or your birthday. Learn to combine letters, numbers, and symbols.</p>
          </div>

          <div class="bg-green-50 border-l-4 border-green-600 p-4 rounded-r-lg">
            <strong class="text-slate-900">Enable Two-Factor Authentication (2FA):</strong>
            <p class="text-slate-700 mt-1">It is an extra layer of security especially for banking and securing your social media accounts.</p>
          </div>

          <div class="bg-green-50 border-l-4 border-green-600 p-4 rounded-r-lg">
            <strong class="text-slate-900">Refrain from using public Wi-Fi for transactions:</strong>
            <p class="text-slate-700 mt-1">Some public networks (like in malls or cafés) are often unprotected and prone to cyberattacks.</p>
          </div>

          <div class="bg-green-50 border-l-4 border-green-600 p-4 rounded-r-lg">
            <strong class="text-slate-900">Update apps and devices regularly:</strong>
            <p class="text-slate-700 mt-1">Updates resolve security problems that hackers could exploit.</p>
          </div>

          <div class="bg-green-50 border-l-4 border-green-600 p-4 rounded-r-lg">
            <strong class="text-slate-900">Be cautious on using social media:</strong>
            <p class="text-slate-700 mt-1">Do not share too much personal information, especially your exact location or financial details.</p>
          </div>
        </div>

        <p class="text-lg text-slate-700 leading-relaxed mb-6">
          By applying these practices, us Filipinos can reduce the risk of becoming victims of internet threats.
        </p>

