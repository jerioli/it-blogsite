          <p class="text-slate-700 mb-2">
            For instance, someone may receive a message from a "friend" asking for help in an emergency. Because of trust and concern, they send money immediately—only to find out later that the account was hacked. In this case, it wasn't technology that failed, but human emotion.
          </p>
        </div>

        <p class="text-lg text-slate-700 leading-relaxed mb-6">
          In online buying and selling, scammers often rush their victims. They might say, "Pay now or the item will be gone," or "Send this first so I can process your order." This pressure makes people act fast without thinking, leading to poor decisions.
        </p>

        <p class="text-lg text-slate-700 leading-relaxed mb-6">
          Curiosity is also a powerful tool. A simple message like "Is this you?" with a link can make someone click instantly. That one click can give hackers access to personal accounts and information.
        </p>

        <div class="bg-gradient-to-r from-slate-900 to-slate-800 text-white p-6 rounded-xl mb-6 border border-purple-500">
          <p class="text-lg mb-0">
