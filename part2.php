        <p class="text-sm sm:text-base md:text-lg text-slate-700 leading-relaxed mb-4 sm:mb-6">
          The internet has become an essential part of our daily lives, but behind its convenience lie hidden dangers that many people are not aware of. From malware and ransomware to phishing and identity theft, cyber threats are constantly evolving. These dangers target individuals and organizations alike, exploiting our lack of awareness or carelessness online.
        </p>

        <p class="text-lg text-slate-700 leading-relaxed mb-6">
          Phishing is one of the most common tactics used by cybercriminals. They send fake emails or messages pretending to be from banks, government agencies, or even friends, trying to trick people into giving personal information or clicking malicious links.
        </p>

        <p class="text-lg text-slate-700 leading-relaxed mb-6">
          Malware and ransomware can infect devices silently, stealing files, locking systems, or demanding payments to regain access.
        </p>

        <p class="text-lg text-slate-700 leading-relaxed mb-6">
          Another hidden threat is social engineering, where hackers manipulate human behavior rather than technical weaknesses. They may post as trusted sources or create fake emergencies, preying on people's emotions to make them act without thinking. Combined with oversharing on social media, these actions can put anyone at risk—your accounts, private information, and even financial security.
        </p>

        <p class="text-lg text-slate-700 leading-relaxed mb-6">
          Understanding these dangers is the first step in staying safe online. Awareness, combined with cautious behavior, is key to reducing the risk of falling victim to these invisible threats.
        </p>

